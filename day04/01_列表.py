# list类型
name_list = ['TT', 'Vv']
print(name_list)
print(type(name_list))

# 列表可以存储不同类型的数据
my_list = [1, 'Rr', [0, 1], True]
print(my_list)

# 定义嵌套列表
two_list = [[0, 1, 2, 3], [4, 5]]
print(two_list)

# 列表索引
list_1 = [1, 2, 3, 4]
# 取数字 1
print(list_1[0])
print(list_1[-4])

# 嵌套列表取数字
list_2 = [[0, 1, 2, 3], [4, 5]]
# 取出5
print(list_2[1][1])
